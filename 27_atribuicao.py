# Relembrando os operadores:
x = 5
print(f"Valor de x: {x}")
x = x + 3
print(f"Valor de x após reescrita: {x}")
x = x - 3
print(f"Valor de x após reescrita: {x}")
x = x * 3
print(f"Valor de x após reescrita: {x}")
x = x ** 3
print(f"Valor de x após reescrita: {x}")
x = x / 3
print(f"Valor de x após reescrita: {x}")
x = x // 3
print(f"Valor de x após reescrita: {x}")
x = x % 3
print(f"Valor de x após reescrita: {x}")

# Posso fazer atribuições da seguinte forma:
x = 3
x += 3 # Equivalente a x = x + 3
x -= 3 # Equivalente a x = x - 3
x *= 3 # Equivalente a x = x * 3
x **= 3 # Equivalente a x = x ** 3
x /= 3 # Equivalente a x = x / 3
x //= 3 # Equivalente a x = x // 3
x %= 3 # Equivalente a x = x % 3
print(x)
