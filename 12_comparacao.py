"""
Operadores de comparação:

Operador de igualdade: ==
Operador de diferença: !=
Operador maior que: >
Operador menor que: <
Operador maior ou igual a: >=
Operador menor ou igual a: <=
"""

x = 3 #Tipo int
y = 5
z = 3.0 #Tipo float
w = 8

print(x == y)
print(x == z)
print(x != y)
print(x < y)
print(x > y)
print(x >= y)
print(x <= y)
print(w >= y)
print(w <= y)
